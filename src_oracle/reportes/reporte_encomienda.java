    private void jToggleButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jToggleButton1ActionPerformed
        Document document = new Document();
        try {
            // Ruta de salida del archivo PDF
            String rutaSalida = "d:\\reportes net\\EncominedaReporte.pdf"; // Cambiar ruta aquí
            PdfWriter.getInstance(document, new FileOutputStream(rutaSalida));
            document.open();

            // Título del documento
            Paragraph title = new Paragraph("Reporte de encomienda");
            title.setAlignment(Element.ALIGN_CENTER);
            document.add(title);
            document.add(new Paragraph(" ")); // Espacio entre título y tabla

            // Usar la conexión existente
            conectar conn = new conectar();  // Instanciar tu clase de conexión
            Connection connection = conn.optenerConexion();  // Obtener la conexión

            // Verificar si la conexión es exitosa
            if (connection != null) {
                Statement statement = connection.createStatement();
                String query = "SELECT * FROM encomienda";
                ResultSet resultSet = statement.executeQuery(query);

                // Crear una tabla PDF
                PdfPTable table = new PdfPTable(7); // 3 columnas

                // Encabezados de la tabla
                PdfPCell cell1 = new PdfPCell(new Phrase("id "));
                PdfPCell cell2 = new PdfPCell(new Phrase("descripcion"));
                PdfPCell cell3 = new PdfPCell(new Phrase("destino"));
                PdfPCell cell4 = new PdfPCell(new Phrase("precio"));
                PdfPCell cell5 = new PdfPCell(new Phrase("ci destinatario"));
                PdfPCell cell6 = new PdfPCell(new Phrase("num placa"));
                PdfPCell cell7 = new PdfPCell(new Phrase("ci vendedora"));
                

                table.addCell(cell1);
                table.addCell(cell2);
                table.addCell(cell3);
                table.addCell(cell4);
                table.addCell(cell5);
                table.addCell(cell6);
                table.addCell(cell7);

                // Llenar la tabla con los datos
                while (resultSet.next()) {
                    table.addCell(resultSet.getString("id_encomienda"));
                    table.addCell(resultSet.getString("descripcion"));
                    table.addCell(resultSet.getString("destino"));
                    table.addCell(resultSet.getString("precio"));
                    table.addCell(resultSet.getString("ci_destinatario"));
                    table.addCell(resultSet.getString("num_placa"));
                    table.addCell(resultSet.getString("ci_vendedora"));
                }
                // Agregar la tabla al documento
                document.add(table);
                document.close();

                // Abrir el PDF generado
                File pdfFile = new File(rutaSalida);
                if (Desktop.isDesktopSupported()) {
                    Desktop.getDesktop().open(pdfFile);
                } else {
                    System.out.println("El sistema no soporta la apertura de archivos.");
                }

                // Cerrar conexión
                conn.desconectar();
            } else {
                System.out.println("Error al conectar con la base de datos.");
            }

        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_jToggleButton1ActionPerformed


